package com.site.ex;

public class Salary {
	int num;
	String name;
	String id;
	String pw;
	String tel;
	String gender;
	int salary1;
	int minus;
	
	public Integer getSalary1() {
		return salary1;
	}
	public void setSalary1(int salary1) {
		this.salary1 = salary1;
	}
	public Integer getMinus() {
		return minus;
	}
	public void setMinus(int minus) {
		this.minus = minus;
	}
	public Integer getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	
	
}
